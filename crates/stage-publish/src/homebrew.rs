/// Parameters for cask generation.
pub struct CaskParams<'a> {
    /// Cask token (lowercase hyphenated name used as `cask "name"`).
    pub name: &'a str,
    /// Display name shown in `name` stanza.
    pub display_name: &'a str,
    /// Alternative display names.
    pub alternative_names: &'a [String],
    /// Release version string.
    pub version: &'a str,
    /// SHA-256 hex digest of the download artifact.
    pub sha256: &'a str,
    /// Download URL for the .dmg/.zip/.app archive.
    pub url: &'a str,
    /// Project homepage URL.
    pub homepage: Option<&'a str>,
    /// One-line description.
    pub description: Option<&'a str>,
    /// macOS .app bundle name (e.g. "MyApp.app").
    pub app: Option<&'a str>,
    /// Binary stubs to create in /usr/local/bin (inside the .app).
    pub binaries: &'a [String],
    /// Post-install user-facing notes.
    pub caveats: Option<&'a str>,
    /// Zap stanza paths for complete uninstall cleanup.
    pub zap: &'a [String],
    /// Uninstall stanza directives (e.g. `quit: "com.example.App"`).
    pub uninstall: &'a [String],
}

/// Generate a Homebrew Cask Ruby file string from the given parameters.
pub fn generate_cask(params: &CaskParams<'_>) -> String {
    let mut tera = tera::Tera::default();
    tera.add_raw_template("cask", CASK_TEMPLATE)
        .expect("homebrew: parse cask template");
    tera.autoescape_on(vec![]);

    let mut ctx = tera::Context::new();
    ctx.insert("name", params.name);
    ctx.insert("display_name", params.display_name);
    ctx.insert("alternative_names", params.alternative_names);
    ctx.insert("version", params.version);
    ctx.insert("sha256", params.sha256);
    ctx.insert("url", params.url);
    ctx.insert("homepage", &params.homepage.unwrap_or(""));
    ctx.insert("description", &params.description.unwrap_or(""));
    ctx.insert("app", &params.app.unwrap_or(""));
    ctx.insert("binaries", params.binaries);

    let has_caveats = params.caveats.is_some();
    ctx.insert("has_caveats", &has_caveats);
    ctx.insert("caveats", params.caveats.unwrap_or(""));

    let has_zap = !params.zap.is_empty();
    ctx.insert("has_zap", &has_zap);
    ctx.insert("zap", params.zap);

    let has_uninstall = !params.uninstall.is_empty();
    ctx.insert("has_uninstall", &has_uninstall);
    ctx.insert("uninstall_items", params.uninstall);

    tera.render("cask", &ctx)
        .expect("homebrew: render cask template")
}

