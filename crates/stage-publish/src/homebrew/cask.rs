//! Homebrew cask generation: template, params struct, and the per-context
//! cask body builder used by both per-crate and top-level publishers.
use super::formula::build_uninstall_directives;
use anodizer_core::context::Context;
use anyhow::{Context as _, Result};
const CASK_TEMPLATE: &str = r#"# This file was generated by anodizer. DO NOT EDIT.
cask "{{ name }}" do
  version "{{ version }}"
{% if not has_platforms %}  sha256 "{{ sha256 }}"

  url "{{ url }}"
{% endif %}{% if custom_block %}{{ custom_block }}
{% endif %}{% if homepage %}  homepage "{{ homepage }}"
{% endif %}  name "{{ display_name }}"
{% for alt in alternative_names %}  name "{{ alt }}"
{% endfor %}{% if description %}  desc "{{ description }}"
{% endif %}
  livecheck do
    skip "Auto-generated on release."
  end

{% if has_platforms %}{% for plat in platforms %}  on_{{ plat.os_block }} do
{% for arch_entry in plat.arches %}    on_{{ arch_entry.arch_block }} do
      url "{{ arch_entry.url }}"
      sha256 "{{ arch_entry.sha256 }}"
    end
{% endfor %}  end

{% endfor %}{% endif %}{% if has_depends %}{% for dep in depends_on %}  depends_on {{ dep }}
{% endfor %}{% endif %}{% if has_conflicts %}  conflicts_with {% for c in conflicts_with %}{% if not loop.first %}                  {% endif %}{{ c }}{% if not loop.last %},
{% endif %}{% endfor %}

{% endif %}{% if app %}  app "{{ app }}"
{% endif %}{% for binary in binaries %}  binary "{{ binary }}"
{% endfor %}{% for manpage in manpages %}  manpage "{{ manpage }}"
{% endfor %}{% if has_completions_bash %}  bash_completion "{{ completions_bash }}"
{% endif %}{% if has_completions_zsh %}  zsh_completion "{{ completions_zsh }}"
{% endif %}{% if has_completions_fish %}  fish_completion "{{ completions_fish }}"
{% endif %}{% if has_service %}  service do
{{ service }}
  end
{% endif %}{% if has_preflight %}  preflight do
{{ preflight }}
  end
{% endif %}{% if has_postflight %}  postflight do
{{ postflight }}
  end
{% endif %}{% if has_uninstall_preflight %}  uninstall_preflight do
{{ uninstall_preflight }}
  end
{% endif %}{% if has_uninstall_postflight %}  uninstall_postflight do
{{ uninstall_postflight }}
  end
{% endif %}{% if has_caveats %}
  caveats <<~EOS
    {{ caveats }}
  EOS
{% endif %}{% if has_uninstall %}  uninstall {% for u in uninstall_items %}{% if not loop.first %}            {% endif %}{{ u }}{% if not loop.last %},
{% endif %}{% endfor %}

{% endif %}{% if has_zap %}  zap trash: [
{% for z in zap %}    "{{ z }}",
{% endfor %}  ]
{% endif %}end
"#;

