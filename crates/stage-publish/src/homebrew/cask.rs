/// Helper: render one keyed group of items as a Ruby array literal.
///
/// ```ruby
/// launchctl: [
///       "com.example.daemon",
///     ]
/// ```
fn group_to_ruby_array(name: &str, lines: &[String]) -> String {
    let mut sb = String::new();
    sb.push_str(name);
    sb.push_str(": [\n");
    for l in lines {
        sb.push_str("      ");
        // Use Rust's `Debug` formatting for `&str` to get Ruby-compatible
        // double-quoted, backslash-escaped output for ASCII-safe strings.
        sb.push_str(&format!("{:?}", l));
        sb.push_str(",\n");
    }
    sb.push_str("    ]");
    sb
}

/// Helper: join the per-key groups into a single `<stanza> ...` Ruby block.
fn join_groups(stanza: &str, groups: &[String]) -> String {
    if groups.is_empty() {
        return String::new();
    }
    let mut sb = String::new();
    sb.push_str(stanza);
    sb.push(' ');
    for (i, group) in groups.iter().enumerate() {
        if i > 0 {
            sb.push_str("    ");
        }
        sb.push_str(group);
        if i < groups.len() - 1 {
            sb.push(',');
        }
        sb.push('\n');
    }
    // Drop the trailing newline so the template can decide spacing.
    if sb.ends_with('\n') {
        sb.pop();
    }
    sb
}

fn make_uninstall_like_groups(u: &anodizer_core::config::HomebrewCaskUninstall) -> Vec<String> {
    let mut groups = Vec::new();
    if let Some(ref items) = u.launchctl
        && !items.is_empty()
    {
        groups.push(group_to_ruby_array("launchctl", items));
    }
    if let Some(ref items) = u.quit
        && !items.is_empty()
    {
        groups.push(group_to_ruby_array("quit", items));
    }
    if let Some(ref items) = u.login_item
        && !items.is_empty()
    {
        groups.push(group_to_ruby_array("login_item", items));
    }
    if let Some(ref items) = u.delete
        && !items.is_empty()
    {
        groups.push(group_to_ruby_array("delete", items));
    }
    if let Some(ref items) = u.trash
        && !items.is_empty()
    {
        groups.push(group_to_ruby_array("trash", items));
    }
    groups
}

/// Render the full `uninstall <key>: [...], <key>: [...]` Ruby block from
/// structured uninstall config. Returns `""` when no uninstall config is
/// configured.
pub(super) fn render_uninstall_block(
    uninstall: Option<&anodizer_core::config::HomebrewCaskUninstall>,
) -> String {
    let Some(u) = uninstall else {
        return String::new();
    };
    let groups = make_uninstall_like_groups(u);
    join_groups("uninstall", &groups)
}

/// Render the full `zap <key>: [...], <key>: [...]` Ruby block from
/// structured zap config. Unlike `uninstall`, an empty config still
/// emits a `# No zap stanza required` comment so the cask file shape
/// stays identical whether or not a zap stanza is configured.
pub(super) fn render_zap_block(
    zap: Option<&anodizer_core::config::HomebrewCaskUninstall>,
) -> String {
    let Some(u) = zap else {
        return String::new();
    };
    let groups = make_uninstall_like_groups(u);
    if groups.is_empty() {
        return String::new();
    }
    join_groups("zap", &groups)
}

