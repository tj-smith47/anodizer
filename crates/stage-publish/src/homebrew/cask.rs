/// Render Ruby kwargs for the `url "<url>"` line: `verified`, `using`,
/// `cookies`, `referer`, `header`, `user_agent`, `data`. Returns `""` when
/// every sub-field is unset.
///
/// `indent` is the leading whitespace placed before each kwarg (6 spaces
/// at top level, 8 spaces when the call site is inside an
/// `on_<arch> do ... end` block). The returned string starts with `,\n`
/// + indent so it splices directly after the closing `"` of `url "..."`.
pub(super) fn render_additional_url_params(
    u: &anodizer_core::config::HomebrewCaskURL,
    indent: &str,
) -> String {
    let mut parts: Vec<String> = Vec::new();
    if let Some(ref v) = u.verified
        && !v.is_empty()
    {
        parts.push(format!("verified: \"{}\"", v));
    }
    if let Some(ref v) = u.using
        && !v.is_empty()
    {
        // `using:` accepts either a Ruby symbol (`:homebrew_curl`) or a
        // class name (no quotes). Pass through verbatim without quoting.
        parts.push(format!("using: {}", v));
    }
    if let Some(ref cookies) = u.cookies
        && !cookies.is_empty()
    {
        let mut hash = String::from("cookies: {\n");
        // Sort keys so output is deterministic across runs (HashMap iter
        // order would otherwise produce noisy diffs in committed cask
        // files).
        let mut keys: Vec<_> = cookies.keys().collect();
        keys.sort();
        for k in keys {
            let v = &cookies[k];
            hash.push_str(indent);
            hash.push_str(&format!("  \"{}\" => \"{}\",\n", k, v));
        }
        hash.push_str(indent);
        hash.push('}');
        parts.push(hash);
    }
    if let Some(ref v) = u.referer
        && !v.is_empty()
    {
        parts.push(format!("referer: \"{}\"", v));
    }
    if let Some(ref headers) = u.headers
        && !headers.is_empty()
    {
        let mut arr = String::from("header: [\n");
        for h in headers {
            arr.push_str(indent);
            arr.push_str(&format!("  \"{}\",\n", h));
        }
        arr.push_str(indent);
        arr.push(']');
        parts.push(arr);
    }
    if let Some(ref v) = u.user_agent
        && !v.is_empty()
    {
        parts.push(format!("user_agent: \"{}\"", v));
    }
    if let Some(ref data) = u.data
        && !data.is_empty()
    {
        let mut hash = String::from("data: {\n");
        let mut keys: Vec<_> = data.keys().collect();
        keys.sort();
        for k in keys {
            let v = &data[k];
            hash.push_str(indent);
            hash.push_str(&format!("  \"{}\" => \"{}\",\n", k, v));
        }
        hash.push_str(indent);
        hash.push('}');
        parts.push(hash);
    }

    if parts.is_empty() {
        return String::new();
    }
    let mut out = String::new();
    for p in &parts {
        out.push_str(",\n");
        out.push_str(indent);
        out.push_str(p);
    }
    out
}

/// Split rendered `alternative_names` into two groups:
///
/// - **Aliases**: pure-string alternative names that Homebrew treats as
///   aliases — they render as additional `name "..."` directives INSIDE
///   the primary cask file.
/// - **Versioned files**: alternative names that contain an `@` — these
///   emit a SECOND `.rb` file named after the alt entry (e.g.
///   `myapp@1.2.3.rb`) so users can `brew install myapp@1.2.3` for a
///   downgrade path.
///
/// The split follows `alternative_names:` semantics: the example
/// `myproject@{{ .Version }}` only makes sense as a separate file, since
/// using it as an alias would mean every release overwrites the previous
/// version's record.
pub(super) fn split_alternative_names(
    rendered: &[String],
    cask_name: &str,
) -> (Vec<String>, Vec<String>) {
    let mut aliases: Vec<String> = Vec::new();
    let mut versioned: Vec<String> = Vec::new();
    for entry in rendered {
        let trimmed = entry.trim();
        if trimmed.is_empty() || trimmed == cask_name {
            continue;
        }
        if trimmed.contains('@') {
            versioned.push(trimmed.to_string());
        } else {
            aliases.push(trimmed.to_string());
        }
    }
    (aliases, versioned)
}

/// Pre-render every `alternative_names` entry through the user's template
/// engine. A failure surfaces as `Err` so a typo in the template doesn't
/// silently degrade the rendered cask body.
pub(super) fn render_alternative_names(ctx: &Context, raw: &[String]) -> Result<Vec<String>> {
    let mut out: Vec<String> = Vec::with_capacity(raw.len());
    for entry in raw {
        let rendered = ctx.render_template(entry).with_context(|| {
            format!("homebrew cask: render alternative_names entry '{}'", entry)
        })?;
        out.push(rendered);
    }
    Ok(out)
}

/// Generate a Homebrew Cask Ruby file string from the given parameters.
pub fn generate_cask(params: &CaskParams<'_>) -> Result<String> {
    let tera = anodizer_core::template::parse_static("cask", CASK_TEMPLATE)
        .context("homebrew: parse cask template")?;

    let mut ctx = tera::Context::new();
    ctx.insert("name", params.name);
    ctx.insert("display_name", params.display_name);
    ctx.insert("alternative_names", params.alternative_names);
    ctx.insert("version", params.version);
    ctx.insert("sha256", params.sha256);
    ctx.insert("url", params.url);
    ctx.insert("url_extras", params.url_extras);
    ctx.insert("url_extras_indented", params.url_extras_indented);

    // Multi-platform support
    let has_platforms = !params.platforms.is_empty();
    ctx.insert("has_platforms", &has_platforms);
    ctx.insert("platforms", &params.platforms);
    // Optional cask stanzas — every `unwrap_or("")` below pairs with either a
    // `{% if foo %}` truthy-guard or a sibling `has_<foo>` boolean in
    // CASK_TEMPLATE (see top of file). Empty values render as no-stanza in the
    // Ruby output. Homebrew Cask DSL accepts cask blocks without homepage,
    // desc, caveats, service, hooks, etc. — none are required by `brew style`.
    ctx.insert("homepage", &params.homepage.unwrap_or(""));
    ctx.insert("description", &params.description.unwrap_or(""));
    ctx.insert("app", &params.app.unwrap_or(""));
    ctx.insert("binaries", params.binaries);

    let has_caveats = params.caveats.is_some();
    ctx.insert("has_caveats", &has_caveats);
    ctx.insert("caveats", params.caveats.unwrap_or(""));

    let has_zap = !params.zap_block.is_empty();
    ctx.insert("has_zap", &has_zap);
    ctx.insert("zap_block", params.zap_block);

    let has_uninstall = !params.uninstall_block.is_empty();
    ctx.insert("has_uninstall", &has_uninstall);
    ctx.insert("uninstall_block", params.uninstall_block);

    // Custom block
    ctx.insert("custom_block", &params.custom_block.unwrap_or(""));

    // Service
    let has_service = params.service.is_some();
    ctx.insert("has_service", &has_service);
    ctx.insert("service", params.service.unwrap_or(""));

    // Manpages
    ctx.insert("manpages", params.manpages);

    // Completions
    let has_completions_bash = params.completions_bash.is_some();
    let has_completions_zsh = params.completions_zsh.is_some();
    let has_completions_fish = params.completions_fish.is_some();
    ctx.insert("has_completions_bash", &has_completions_bash);
    ctx.insert("completions_bash", params.completions_bash.unwrap_or(""));
    ctx.insert("has_completions_zsh", &has_completions_zsh);
    ctx.insert("completions_zsh", params.completions_zsh.unwrap_or(""));
    ctx.insert("has_completions_fish", &has_completions_fish);
    ctx.insert("completions_fish", params.completions_fish.unwrap_or(""));

    // Dependencies
    let has_depends = !params.depends_on.is_empty();
    ctx.insert("has_depends", &has_depends);
    ctx.insert("depends_on", params.depends_on);

    // Conflicts
    let has_conflicts = !params.conflicts_with.is_empty();
    ctx.insert("has_conflicts", &has_conflicts);
    ctx.insert("conflicts_with", params.conflicts_with);

    // Hooks
    let has_preflight = params.preflight.is_some();
    let has_postflight = params.postflight.is_some();
    let has_uninstall_preflight = params.uninstall_preflight.is_some();
    let has_uninstall_postflight = params.uninstall_postflight.is_some();
    ctx.insert("has_preflight", &has_preflight);
    ctx.insert("preflight", params.preflight.unwrap_or(""));
    ctx.insert("has_postflight", &has_postflight);
    ctx.insert("postflight", params.postflight.unwrap_or(""));
    ctx.insert("has_uninstall_preflight", &has_uninstall_preflight);
    ctx.insert(
        "uninstall_preflight",
        params.uninstall_preflight.unwrap_or(""),
    );
    ctx.insert("has_uninstall_postflight", &has_uninstall_postflight);
    ctx.insert(
        "uninstall_postflight",
        params.uninstall_postflight.unwrap_or(""),
    );

    // generate_completions_from_executable (rendered after postflight:
    // postflight may strip the macOS quarantine attribute, which must run
    // before Homebrew executes the binary to generate completions).
    let has_generate_completions = params.generate_completions.is_some();
    ctx.insert("has_generate_completions", &has_generate_completions);
    ctx.insert(
        "generate_completions",
        params.generate_completions.as_deref().unwrap_or(""),
    );

    anodizer_core::template::render_static(&tera, "cask", &ctx, "homebrew")
}

